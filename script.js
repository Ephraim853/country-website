// DOM elements
const modal = document.getElementById('pdf-modal');
const modalTitle = document.getElementById('modal-title');
const pdfContent = document.getElementById('pdf-content');
const closeBtn = document.querySelector('.close-btn');
const viewButtons = document.querySelectorAll('.view-btn');

// Open modal with PDF content
function openPdf(pdfId) {
    const pdf = pdfData[pdfId];
    if (!pdf) return;
    
    modalTitle.textContent = pdf.title;
    pdfContent.innerHTML = '';
    
    // Create pages for the PDF
    pdf.pages.forEach((page, index) => {
        const pageElement = document.createElement('div');
        pageElement.className = 'pdf-page';
        pageElement.innerHTML = `
            <h3>${page.title}</h3>
            <p>${page.content}</p>
        `;
        pdfContent.appendChild(pageElement);
    });
    
    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';
}

// Close modal
function closeModal() {
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
}

// Event listeners
viewButtons.forEach(button => {
    button.addEventListener('click', () => {
        const pdfId = button.getAttribute('data-pdf');
        openPdf(pdfId);
    });
});

closeBtn.addEventListener('click', closeModal);

// Close modal when clicking outside content
window.addEventListener('click', (event) => {
    if (event.target === modal) {
        closeModal();
    }
});

// Close modal with Escape key
document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
        closeModal();
    }
});
